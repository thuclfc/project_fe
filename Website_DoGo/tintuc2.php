                                <h1 style="text-align: center; color: red; font-size: large;">VÌ SAO NHÀ ĐẦU TƯ KHÓ TIẾP CẬN VỚI ĐẤT TRỒNG RỪNG SẢN XUẤT ?</h1>
                                <p style="color: black; font-size: small;">Việt Nam có diện tích đất lâm nghiệp bình quân theo đầu người rất thấp: 0,22 ha/ người, so với thế giới chỉ bằng 23%.Nếu chỉ tính diện tích đất lâm nghiệp để sản xuất kinh doanh thì con số này còn thấp hơn nhiều.</p>
                                <p style="color: black; font-size: small;">Thế nhưng đến nay, diện tích đất trống đồi trọc được trồng rừng chỉ chiếm 39%, còn hơn 60% bị bỏ hoá hoặc sử dụng ngoài mục đích lâm nghiệp, đất trồng rừng sản xuất thì lại đang thiếu vốn đầu tưĐất trồng rừng là tài sản (bất động sản) của hộ gia đình và doanh nghiệp trồng rừng. Theo quy luật kinh tế thị trường, phát triển trồng rừng sản xuất và thị trường gỗ nguyên liệu tất yếu sẽ dẫn đến việc tích tụ đất đai của các đơn vị sản xuất lâm nghiệp nhằm nâng cao hiệu quả sản xuất kinh doanh.</p>
                                <p style="text-align: center;"><img  src="image/1286249262-287473.jpg"/></p>
                                <p style="text-align: center;"><img src="image/1284083463.jpg" /></p>
                                <p style="color: black; font-size: small;">Ở nước ta, trừ vùng miền núi Bắc Bộ, việc giao rừng cho tổ chức và hộ gia đình được tiến hành song song, còn ở các vùng khác chỉ mới giao rừng cho các tổ chức nhà nước là chính, giao rừng cho hộ gia đình rất ít, thậm chí không giao rừng tự nhiên.

Tiến độ thực hiện giao rừng cho hộ gia đình trên các vùng miền, tỉnh cũng rất khác nhau: miền núi phía Bắc giao được nhiều nhất 2,068 triệu ha, chiếm 56% tổng diện tích rừng đã giao cho hộ; vùng Bắc Trung Bộ: 800 nghìn ha, đạt 22%; vùng Duyên hải Nam Trung Bộ là 13%; các vùng còn lại diện tích rừng giao cho hộ rất hạn chế.</p>
                                <p style="color: black; font-size: small;">Theo định hướng của Chiến lược phát triển lâm nghiệp đến năm 2020, cần trồng mới 2 triệu ha rừng để đáp ứng được nhu cầu gỗ nguyên liệu và có độ che phủ rừng toàn quốc 47%, nhu cầu vốn đầu tư hàng nghìn tỷ đồng, trong đó khoảng 60% phải khai thác vốn trong dân và các nhà đầu tư.

Nhưng hiện nay, các nhà đầu tư tiềm năng, cả trong và ngoài nước, có ý đồ đầu tư vào trồng rừng sản xuất với quy mô lớn lại rất khó tiếp cận với đất trồng rừng sản xuất.

Để đáp ứng đà tăng trưởng xuất khẩu sản phẩm gỗ của ngành chế biến, nhu cầu nhập khẩu gỗ có xu hướng tăng lên trong tương lai và giá cả cũng tăng do chi phí vận tải tăng. Đây là một bất lợi lớn nhất làm giảm sức cạnh tranh của ngành chế biến xuất khẩu đồ gỗ của Việt Nam.</p>
                                <p style="color: black; font-size: small;">Nhiều doanh nghiệp chế biến gỗ trong nước rất muốn đầu tư trồng rừng để chủ động về nguồn nguyên liệu, giảm chi phí về nguyên liệu, tăng sức cạnh tranh, nhưng không mấy dễ dàng tìm được đất trồng rừng để bỏ vốn đầu tư.



Đến nay chỉ duy nhất có Công ty trồng rừng Quy Nhơn (100% vốn Nhật Bản) được thuê đất, đã trồng thành công gần 10.000 ha rừng, muốn mở rộng diện tích trồng rừng hơn nữa nhưng không thuê thêm được đất, kể cả lên Tây Nguyên.

Thói quen dựa vào vốn từ ngân sách

Nói về những tồn tại này, ông Vũ Long, Viện Nghiên cứu và Phát triển lâm nghiệp nhiệt đới phân tích: nguyên nhân đầu tiên là do việc thực hiện chỉ tiêu kế hoạch nhà nước kèm theo vốn đầu tư của ngân sách gần như đã thành một nếp hoạt động quen thuộc của các cơ quan quản lý lâm nghiệp. Họ chỉ tập trung nguồn lực của mình vào thực hiện các nhiệm vụ kế hoạch nhà nước có vốn đầu của ngân sách, hoặc vốn viện trợ nước ngoài.

Trong Dự án trồng mới 5 triệu ha rừng, chỉ tiêu trồng rừng sản xuất đạt rất thấp, vì không có vốn đầu tư trực tiếp của ngân sách, mà thông qua vốn tín dụng và tín dụng đầu tư. Các doanh nghiệp, người trồng rừng phải tự lo từ A - Z, nhận được rất ít sự quan tâm, trợ giúp của cơ quan lâm nghiệp và chính quyền địa phương. Hầu như không có một địa phương nào nêu ra được một dự án đầu tư cụ thể.

Ở bất kỳ cuộc hội nghị lâm nghiệp về phát triển trồng rừng sản xuất nào, "bài ca" muôn thuở thường được nêu ra là thiếu vốn đầu tư, giải pháp duy nhất là đề nghị Nhà nước tăng vốn tín dụng đầu tư trồng rừng lãi suất thấp. Còn bằng những giải pháp nào để khai thác được nguồn vốn trong dân và nhà đầu tư nước ngoài thì không được bàn đến nơi đến chốn.

Một số tư nhân đã bỏ vốn liên kết với dân trồng rừng nguyên liệu đã gặp rất nhiều trở ngại, thậm chí thất bại, nhưng không được các cơ quan Nhà nước giúp đỡ tháo gỡ khó khăn, nên đã bỏ cuộc, nản lòng.

Để khắc phục tình trạng trên, các tỉnh cần phải công khai hóa đất trồng rừng sản xuất xuất được quy hoạch. Sau khi việc rà soát 3 loại rừng cấp tỉnh đã được phê duyệt phải đóng mốc ranh giới trên thực địa (trên từng địa bàn xã), ưu tiên đóng mốc trước ở những nơi có quỹ đất trồng rừng sản xuất tập trung, có thị trường gỗ nguyên liệu; sớm thực hiện việc cho thuê đất lâm nghiệp sản xuất đối với tất cả các tổ chức kinh tế.</p>
                        <p style="color: black; font-size: small;">Thông qua việc cho thuê đất mới thực sự thu hồi được hàng trăm ngàn ha đất không được sử dụng đang do các công ty lâm nghiệp nhà nước nắm giữ; tổ chức hội nghị khách hàng (các nhà đầu tư trồng rừng tiềm năng, trước hết nhắm đến các doanh nghiệp chế biến gỗ lớn, đang phải nhập khẩu gỗ) để thăm dò nhu cầu thuê đất lâm nghiệp và giới thiệu các dự án đầu tư lâm nghiệp của địa phương; tổ chức xây dựng các dự án lâm nghiệp để kêu gọi đầu tư.</p>
